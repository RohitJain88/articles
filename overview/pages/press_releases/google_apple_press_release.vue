            <h2>Covid Watch Celebrates Apple and Google’s COVID-19 Contact Tracing Announcement</h2>
            <br>

            <p>April 10, 2020</p>

            <p>
               Covid Watch, a non-profit operating in collaboration with Stanford University, has been working for two months on a contact tracing protocol that will protect users' privacy while tracing the spread of COVID-19. Today, the world’s leading tech companies joined that mission.
            </p>

            <p>
                After discussions with Covid Watch and our partner organizations, Apple and Google <a href="https://www.apple.com/newsroom/2020/04/apple-and-google-partner-on-covid-19-contact-tracing-technology/">announced</a>  today that they are working together for the first time to unify around a common protocol for contact tracing.
            </p>

            <p>
              The <a href="https://www.apple.com/covid19/contacttracing/">specifications</a> shared today make it clear that the standard going forward will be one that respects privacy. Additionally, the cooperation of Apple and Google opens a path to removing significant technological hurdles that organizations have faced while creating Bluetooth-based contact tracing mobile app solutions in the midst of a fast-moving global pandemic.
            </p>

            <p>
              “This is a game-changer,” said Tina White, Executive Director of Covid Watch. “These tools allow for the kind of communication between devices that is absolutely required to perform an effective automated intervention for COVID-19. At the same time, they also set a precedent to make privacy the cornerstone of future contact-tracing efforts.”
            </p>

            <p>
              At the heart of the new changes is a commitment to implement the Temporary Contact Number protocol, also known as TCN. This protocol ensures the anonymity of users who send and receive contact alerts for COVID-19. Under TCN, the connections cannot be traced back to device or user identity. This makes TCN distinct from centralized  models – like Singapore’s TraceTogether – under which users and their contact network could be tracked by the government: a novel and concerning form of surveillance that represents a significant erosion of current privacy laws and norms in many jurisdictions.
            </p>

            <p>
              On March 17, Covid Watch volunteers open sourced a Bluetooth communication protocol – the first to allow for communication between Apple’s iOS and Google’s Android operating systems. Covid Watch then released a <a href="https://www.covid-watch.org/article">white paper</a> describing the privacy model that has since become the broader <a href="https://github.com/TCNCoalition/TCN">TCN protocol</a>. 
            </p>

            <p>
              Many open-source contact-tracing efforts have converged on this model, a situation formalized on April 6 when multiple international efforts coalesced into the larger <a href="https://tcn-coalition.org/">TCN Coalition</a>. As well as providing a common privacy standard, this will ensure that applications from different developers can communicate with one another. 
            </p>

            <p>
              Over the past two weeks, Covid Watch has met with Apple and Google to discuss the TCN protocol, and discussed with Apple in detail the changes needed to implement TCN effectively. Among other issues, contact tracing via Bluetooth required tracing apps to be running in the background on iOS.
            </p>

            <p>
              “These changes address problems we’ve encountered as we’ve poured effort into creating contact tracing protocols ” said Covid Watch co-founder James Petrie. “Looking at what Google and Apple have planned, you can see the influence of this open-source community. We’re excited and grateful that these two leading tech companies have chosen to help us set precedents that protect privacy.”
            </p>

            <p>
              Apple and Google will be releasing these APIs in May, but will likely not have their own mobile apps available until an update in September or October.
            </p>

            <p>
              “We look forward to incorporating these tools from Apple and Google,” said Covid Watch co-founder Zsombor Szabo. 
            </p>

            <p>
              Bluetooth contact tracing <a href="https://science.sciencemag.org/content/early/2020/04/09/science.abb6936">shows great promise</a> to limit the spread of the coronavirus in a community. Oxford university epidemiologists recently showed that digital contact tracing could play a critical role in enabling regions to come out of lockdowns. If 60 percent of people opt in to a functional, secure tracing solution, it could stop the virus when implemented in addition to traditional contact tracing, testing and responsive isolation.
            </p>

            <p>
              “The sooner everyone can all make use of these APIs and protocols, the sooner the world can stop the spread of the disease. We need to move quickly and advocate for apps that use these privacy-preserving protocols to be adopted in jurisdictions around the world,” White said.
            </p>

